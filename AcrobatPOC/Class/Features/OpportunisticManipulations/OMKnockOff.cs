namespace AcrobatPOC.Class.Features.OpportunisticManipulations
{
    public static class OMKnockOff
    {
        private static readonly string FeatureName = "OMKnockOffFeature";
        private static readonly string FeatureGuid = "AB36EF3B-D7D3-4089-A7D7-74701077F997";

        private static readonly string AbilityName = "OMKnockOffAbility";
        private static readonly string AbilityGuid = "32464A84-DED0-4CD9-A794-E4D519D501EC";

        private static readonly string BuffName = "OMKnockOffBuff";
        private static readonly string BuffGuid = "3DDFD7B5-C39A-41C4-9D69-7C8F4E199C27";


        public static void Configure()
        {
            BuffConfigurator.New(BuffName, BuffGuid)
                .SetDisplayName(FeatureName + ".Name")
                .SetDescription(FeatureName + ".Description")
                .AddInitiatorAttackWithWeaponTrigger
                (
                    onlyHit: false, onMiss: true, rangeType: Kingmaker.Enums.WeaponRangeType.MeleeNormal,
                    action : ActionsBuilder.New()
                        .CombatManeuver(onSuccess: null, type: CombatManeuver.Disarm)
                )
                .Configure();

            ActivatableAbilityConfigurator.New(AbilityName, AbilityGuid)
                .SetDisplayName(FeatureName + ".Name")
                .SetDescription(FeatureName + ".Description")
                .SetGroup(OpportunisticManipulationSelection.OpportunisticManipulationAbilityGroup)
                .SetBuff(BuffGuid)
                .Configure();

            FeatureConfigurator.New(FeatureName, FeatureGuid)
                .SetDisplayName(FeatureName + ".Name")
                .SetDescription(FeatureName + ".Description")
                .AddPrerequisiteFeature(FeatureRefs.ImprovedDisarm.Reference.Get())
                .AddFacts
                ([
                    AbilityGuid
                ])
                .Configure();

            

            

        }

        
    }
}
